
public class Chapter09_Ex1 {

	public static void main(String[] args) {
		// 九九
		for (int count = 1; count < 10; count++) {
			for (int count2 = 1; count2 < 10; count2++) {

				System.out.print(count + "*" + count2 + "=" + count * count2 +" ");
			}

			System.out.println();
		}

	}

}
