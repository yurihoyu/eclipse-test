package classmethod.human;

public class Human02 {
	// メンバー変数
			public String name;
			public int age;

		    // 引数なしのコンストラクタ
			public Human02() {
				name = "本田";
				age = 56;
			}
		    // 引数2つのコンストラクタ
			public Human02(String name, int age) {
				this.name = name;
				this.age = age;
			}

}
